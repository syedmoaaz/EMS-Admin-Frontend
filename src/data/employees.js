export const getEmployeeById = (id) =>
  employees.find((employee) => employee.id === id);

export const getFieldEmployees = () =>
  employees.filter(
    (employee) =>
      employee.role === ROLES.ORDER_TAKER ||
      employee.role === ROLES.DISPATCHER
  );

export const getEmployeesByBranch = (branchId) =>
  employees.filter((employee) => employee.branchId === branchId);

export const getActiveEmployeeCount = () =>
  employees.filter((employee) => employee.status === "Active").length;

export const getDesignations = () =>
  [...new Set(employees.map((employee) => employee.designation))].sort();
